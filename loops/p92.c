/* write a program to print a message 5 times using while loop*/

#include<stdio.h>
int main()
{
    int i=1;    // i is called loop variable.. or iterator
    while(i<=5)
    {
        printf("Hello!\n");
        i++;
    }
    return 0;
}

// Note that inside the loops we can do any operation like add subtract , print etc..
